/**
 * Categorization of the evaluation dataset to support targeted benchmarking.
 */
export type DatasetCategory =
  | 'GENERAL_QA'
  | 'CODING'
  | 'MATH'
  | 'TRANSLATION'
  | 'SUMMARIZATION'
  | 'VIETNAMESE_ENTERPRISE'
  | 'HEALTHCARE'
  | 'FINANCE'
  | 'LEGAL';

/**
 * The nature of the dataset.
 */
export type DatasetType =
  | 'GOLDEN' // Curated by humans, the highest quality ground truth.
  | 'SYNTHETIC' // Generated by other LLMs to increase volume.
  | 'ADVERSARIAL' // Specifically designed to trigger prompt injections or safety violations.
  | 'HIDDEN'; // Kept away from prompt engineers to prevent overfitting.

/**
 * A single, immutable test case within a Benchmark Dataset.
 */
export interface TestCase {
  readonly testId: string;
  readonly prompt: string;
  readonly systemPromptContext?: string;
  readonly retrievedContext?: string[]; // Simulates RAG context

  // The exact string expected, or semantic criteria the LLM Judge will use to evaluate.
  readonly expectedOutput: string;
  readonly evaluationCriteria: string[]; // e.g., ["Must mention article 13", "Must be polite"]
}

/**
 * EVAL-002: Benchmark Dataset Aggregate
 *
 * Architecture:
 * Datasets are strictly IMMUTABLE and versioned. Once `finance-legal-v1.0.0` is published,
 * it can never be altered. This guarantees that if we run a regression test 6 months from now,
 * the baseline remains mathematically identical.
 */
export class BenchmarkDataset {
  constructor(
    public readonly datasetId: string,
    public readonly name: string,
    public readonly version: string,
    public readonly category: DatasetCategory,
    public readonly type: DatasetType,
    public readonly testCases: ReadonlyArray<TestCase>,
    public readonly createdAt: Date,
    public readonly author: string,
  ) {}

  /**
   * Prevents execution of empty or malformed datasets.
   */
  public validate(): void {
    if (this.testCases.length === 0) {
      throw new Error(`BenchmarkDataset ${this.datasetId} must contain at least one TestCase.`);
    }

    // Ensure all test cases have valid criteria for the Automatic Judge
    for (const testCase of this.testCases) {
      if (!testCase.expectedOutput || testCase.expectedOutput.trim() === '') {
        throw new Error(`TestCase ${testCase.testId} is missing an expectedOutput.`);
      }
    }
  }

  /**
   * Returns a deterministic subset for fast Canary evaluations,
   * avoiding the cost of running a 10,000-item dataset on every minor PR.
   */
  public getSample(sampleSize: number): ReadonlyArray<TestCase> {
    if (sampleSize >= this.testCases.length) return this.testCases;

    // Deterministic pseudo-random selection (mocked as slice for simplicity here)
    // In production, we use a seeded shuffle to ensure the sample is representative but consistent
    return this.testCases.slice(0, sampleSize);
  }
}

/**
 * Port for the storage and retrieval of Benchmark Datasets.
 */
export interface IBenchmarkDatasetManager {
  getDataset(name: string, version: string): Promise<BenchmarkDataset>;
  listDatasets(category?: DatasetCategory): Promise<BenchmarkDataset[]>;
  publishDataset(dataset: BenchmarkDataset): Promise<void>;
}
